function renderFaq() {
  elements.faqList.innerHTML = faqItems.map(([question, answer]) => `
    <article class="faq-item reveal visible">
      <button type="button" aria-expanded="false">
        <span>${question}</span>
        <span>+</span>
      </button>
      <p>${answer}</p>
    </article>
  `).join('');

  $$('.faq-item button').forEach((button) => {
    button.addEventListener('click', () => {
      const item = button.closest('.faq-item');
      const isActive = item.classList.toggle('active');
      button.setAttribute('aria-expanded', String(isActive));
      button.querySelector('span:last-child').textContent = isActive ? '−' : '+';
    });
  });
}

function setupTheme() {
  const savedTheme = localStorage.getItem('theme') || 'light';
  document.documentElement.dataset.theme = savedTheme;
  $('.theme-toggle').textContent = savedTheme === 'dark' ? '☀' : '☾';

  $('.theme-toggle').addEventListener('click', () => {
    const nextTheme = document.documentElement.dataset.theme === 'dark' ? 'light' : 'dark';
    document.documentElement.dataset.theme = nextTheme;
    localStorage.setItem('theme', nextTheme);
    $('.theme-toggle').textContent = nextTheme === 'dark' ? '☀' : '☾';
  });
}

function setupMenu() {
  const toggle = $('.menu-toggle');
  const links = $('[data-nav-links]');

  toggle.addEventListener('click', () => {
    const isOpen = links.classList.toggle('open');
    toggle.setAttribute('aria-expanded', String(isOpen));
  });

  links.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      links.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    });
  });
}

function setupRevealAnimations() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  $$('.reveal').forEach((element) => observer.observe(element));
}

